                }
            });
        });
    });

    function serializeArrayHelper(e) {
        var t = {};
        return $.each($(e).serializeArray(), function() {
            t[this.name] = this.value
        }), t
    }

    function postRequest(e, t) {
        return $.ajax({
            url: e,
            type: "POST",
            data: t
        })
